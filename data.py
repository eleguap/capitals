class Capitals():
    def __init__(self):
        self.capitals = {
            "custom": CUSTOM,
            "us": US,
            "na": NORTH_AMERICA,
            "sam": SOUTH_AMERICA,
            "caribbean": CARIBBEAN,
            "eu": EUROPE,
            "africa": AFRICA,
            "asia": ASIA,
            "oceania": OCEANIA
        }

    def all(self):
        combined = {}
        for region, region_dict in self.capitals.items():
            if region == "custom":
                continue
            for country, capital in region_dict.items():
                if country not in combined:
                    combined[country] = capital
        return combined
